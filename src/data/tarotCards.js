export function getDailyCard(date = new Date()) {
  const key = `${date.getFullYear()}-${date.getMonth() + 1}-${date.getDate()}`
  const index = [...key].reduce((sum, char) => sum + char.charCodeAt(0), 0) % tarotCards.length
  return tarotCards[index]
}

export function drawCards(count = 3) {
  return [...tarotCards].sort(() => Math.random() - 0.5).slice(0, count)
}
